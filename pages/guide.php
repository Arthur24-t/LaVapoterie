<h2>Quels sont les différents types de vapoteuse ?</h2>
</br></br>
<p>On peut réunir l'ensemble des cigarettes électroniques dans trois grandes familles:</p>
</br>
<ul>
    <ol>   
        <h4>Le format pod :</h4></br>
        <img src="../image/format-pod.jpg" alt="">
        <p>Le Pod, aussi appelé Pod Mod, est une vapoteuse de nouvelle génération. Souvent de petites tailles, il est utilisé pour vapoter des e liquides alternatifs comme les sels de nicotine ou le CBD. Les Pods peuvent également être efficaces avec les e liquides classiques ayant des taux élevés de nicotine et peu de glycérine végétale. Ces e liquides se vapant à faible puissance, les pods n’ont pas besoin de grosse capacité (batterie, résistance) pour être efficace, ce qui explique leur faible encombrement.</p>
    </ol></br></br>
    <ol>
        <h4>Le format tube :</h4></br>
        <img src="../image/format-tube.jpg" alt="">
        <p>La cigarette électronique tube est une vapoteuse de forme tubulaire. Discret et souvent de petite taille, il se glisse facilement dans une poche. Les cigarettes électroniques de cette famille sont composées d’une batterie rechargeable de bonne capacité et d’un clearomiseur, amovible ou non. Il existe en effet les kits de cigarette électronique « AIO » (pour « All-In-One », tout-en-un en français) où le réservoir est directement intégré. Ce format de cigarette électronique est très simple à utiliser. Peu ou pas de réglages, la batterie reconnait automatiquement le clearomiseur et envoie la puissance nécessaire.</p>
    </ol></br></br>
    <ol>
        <h4>Le format box :</h4></br>
        <img src="../image/format-box.jpg" alt="">
        <p>La cigarette électronique box doit son nom à sa batterie au format rectangulaire, comme une « boîte ». Ces kits sont plus encombrants car ils contiennent soit une batterie de très grande capacité, soit des accumulateurs (des piles) spécifiquement adaptés au matériel de vapotage et généralement non fournis. Il existe des « boxs » pouvant contenir plusieurs de ces accus. Ce format permet de vapoter plus longtemps mais aussi d’atteindre des puissances plus élevées.</p>
    </ol></br></br>
</ul>

