<p>Pour faire l’achat de sa première cigarette électronique, il faut avant tout prendre en compte son profil de fumeur. En fonction de la consommation de cigarettes, on en distingue généralement trois :</p>
<ul></br></br>
    <ol><h4>Petit fumeur : moins de 10 cigarettes par jour: </h4></br>
<p>Si vous fumez occasionnellement, vous avez besoin d’une cigarette simple, compacte et efficace. Nous préconisons dans ce cas des vapoteuses de types pod ou tube qui répondront parfaitement à ces critères.</p>
</br></br>
    </ol>
    <ol><h4>Fumeur modéré : entre 10 et 20 cigarettes par jour:</h4></br>
        <p>Si vous êtes un fumeur modéré, vous avez besoin d’une cigarette électronique performante avec une bonne autonomie, tout en restant simple d’utilisation. Nous préconisons dans ce cas des vapoteuses de types tube ou box.</p>
    </ol></br></br>
    <ol><h4>Fumeur infatigable : plus de 20 cigarettes par jour:</h4></br>
        <p>Si vous êtes un fumeur infatigable, vous avez besoin d’une cigarette électronique avec beaucoup d’autonomie pour répondre à vos besoins. Seuls les vapoteuses de type box ont cette particularité.</p>
    </ol></br></br>
</ul>


